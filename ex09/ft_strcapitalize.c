char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[1])
		{
			if (str[i] >= 97 || str[i] <= 122)
				str[i] = str[i] - 32;
			i++;
		}
		if (str[i] == 32)
			str[i]++ - 32;
		i++;
	}
	return (str);
}
